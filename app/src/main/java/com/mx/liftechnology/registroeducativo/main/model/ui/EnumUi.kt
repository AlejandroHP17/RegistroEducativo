/**
 * Enum que representa los diferentes estados de la UI.
 *
 * @author Pelkidev
 * @version 1.0.0
 */
enum class EnumUi {
    /** Estado de carga. */
    LOADING,
    /** Estado de éxito. */
    SUCCESS,
    /** Estado de error. */
    ERROR,
    /** Estado no autorizado. */
    UNAUTHORIZED,
    /** Estado inicial o neutro. */
    NOTHING
}